#include "libft.h"
#include "libftprintf.h"
#include <stdarg.h>
#include <stdio.h>

int	print_format(int c, va_list args);
int	ft_printf(const char *str, ...)
{
	va_list	args;
	int		i;
	int		count;

	count = 0;
	i = 0;
	va_start(args, str);
	while (str[i])
	{
		if (str[i] == '%')
		{
			i++;
			count += print_format(str[i], args);
		}
		else
		{
			ft_putchar_fd(str[i], 1);
			count++;
		}
		i++;
	}
	va_end(args);
	return (count);
}
